                <h2>Elastomers – Industrial Age Rubbers</h2>
                
                <p>The name "silicone" was introduced by Professor F. S. Kipping and his assistants at Nottingham University of England in the early 1900's. Mistakenly taking silicone for the organic ketones, the material was given the name "silicoketones" or "ilicones" for short (Lewis, 1962). As the research intensified, silicone rubber was eventually placed in the proper family of elastomers.</p>
                <p>&nbsp;</p> 
                <p>Elastomers (rubbers) are amorphous polymers that include polyisoprene or (natural rubber) polybutadiene, polyisobutylene, and polyurethanes. Known to have tensile, versatile, and resilient properties, elastomers are used for a variety of uses. Such items include seals, adhesives, and molded flexible parts. Ranging in attributes and applications, elastomers are not made the same. Variables including chemical compatibility, temperature, pressure, and mechanical wear define the usage of a particular elastomer.</p>
