                    <h3>Why Keypads are Made of Silicone Rubber</h3>
                    <p>&nbsp;</p>
                    <p>Given its unique properties, silicone rubber is optimal for the environment of keypads. It enables such products to have the following attributes:</p>
                    <ul>
                    <li><strong>Weather Resistance</strong><br />
                    <p>Keypads made of silicone rubber are well-suited for applications in industrial equipment. This is especially true for those that are exposed to extreme temperatures, or those which may experience weather changes during the manufacturing and delivery process.</p></li>
                    
                    <li><strong>Liquid Resistance</strong><br />
                    <p>Unlike plastic keyboards that are susceptible to liquid damage, silicone keypads stay responsive despite the contact with liquids. Silicone rubber keypads are water-resistant. Such ability to withstand water makes them a priority choice for healthcare institutions where equipment has to be sterilized and cleaned after use.</p></li>
                  
                    <li><strong>Noise Cancellation</strong><br />
                    <p>Since silicone keypads have an elastic structure. They produce zero noise when keys are touched. This feature is excellent for noise-sensitive environments, but it also benefits consumer sector (TV and home theaters), eliminating the potential cause of distraction.</p></li>
                    
                    <li><strong>Maximum Lifecycle</strong><br />
                    <p>Since rubber keypads are highly resistant to corrosion and oxidation. The keypads can last longer than the keypads made of other rubbers. According to M. Goosey (1999), "The silicone elastomer used for the keypad retains its elastic properties over a long life without the adverse changes due to ageing characteristics of other elastomers." Even with the prolonged and active usage, silicone keypads stay resistant to damage.</p></li>
                    
                    <li><strong>Light Penetration and Coloration</strong><br />
                    <p>The silicone compound can be made nearly translucent and still perform as expected. Other elastomers need additional processing to enhance their physical appearance, and are cause for potential opacity.</p></li>
                    
                    <li><strong>Flexibility and Gentle Touch</strong><br />
                    <p>Silicone is a flexible material, which allows product developers to have a full control over appearance and shape of the devices. Silicone-made keypads are able to withstand over a million button operations without deformation. The hardness of the material can be modified allowing for improved comfort of operation.</p></li>
                    
                    </ul>
                    
