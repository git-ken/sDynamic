                <h2>Destiny by Structure</h2>
                
                <p>Silicone rubber is a synthesized elastomer made from a cross-linked silicon-based polymer strengthened by a filler. It is silicone's structure that makes it so unique. Unlike most rubbers that contain polymer chains of carbon, silicone rubbers have silicon in polymer chains instead of carbon. The inorganic foundation of silicone is responsible for silicone's high resistance to fungus and chemicals. With the backbone made of silicon and oxygen found in other high temperature materials such as quartz, glass, and sand, the material has the capacity to withstand extreme exposures. The carbon-to-carbon linkages of most other rubbers leave them susceptible to heat, ozone, and, UV, and other aging factors.</p>
                <p>&nbsp;</p>
                <p>Silicone's structure has a longer bond length and larger bond angles. Given the longer bond length, the molecules make it so the compound can stretch further. In turn, this improves the overall flexibility of the compound. The strong bond between oxygen and silicon results in the compound's inertness.</p>
              
